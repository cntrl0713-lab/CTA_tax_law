type Mode = 'strong' | 'incomplete' | 'half' | 'ambiguous' | 'overgeneralized' | 'partial' | 'merged'

function buildAnswers(fixture: ProblemFixture, mode: Mode): SubquestionAnswer[] {
    const src =
        mode === 'incomplete'
            ? fixture.incompleteAnswers
            : mode === 'half'
                ? fixture.halfAnswers
                : mode === 'ambiguous'
                    ? fixture.ambiguousAnswers
                    : mode === 'overgeneralized'
                        ? fixture.overgeneralizedAnswers
                        : mode === 'partial'
                            ? fixture.partialAnswers
                            : mode === 'merged'
                                ? fixture.mergedAnswers
                                : fixture.strongAnswers
    if (!src) {
        throw new Error(`${fixture.label}은(는) --${mode} 모드를 지원하지 않습니다.`)
    }
    return fixture.problem.cta_subquestion.map((sq) => ({ subquestionNumber: sq.number, answerText: src[sq.number] }))
}

// 정규화: 공백·주요 문장부호 제거 (존재 확인 전용)
function normalize(text: string): string {
    return text.normalize('NFC').replace(/[\s·.,'"“”‘’()[\]「」『』〈〉《》:;!?~\-]/g, '')
}

// gradeProblem.ts의 stripListMarkers와 동일 (독립 재구현): 줄 앞 번호 매기기 기호(1. / (1) / ① / 가. 등) 제거
function stripListMarkers(text: string): string {
    return text.replace(/^[ \t]*(?:\(\d{1,3}\)|\d{1,3}[.)]|[①-⑳]|[가나다라마바사아자차카타파하][.)])[ \t]*/gm, '')
}

// 피드백에서 "'X' 누락 / X 언급이 없 / X이 없" 류 주장을 추출해, 해당 답안에 X가 실제로 존재하면 허위 누락으로 판정
function findFalseOmissions(feedback: string, answerText: string): string[] {
    const normAnswer = normalize(answerText)
    const found: string[] = []
    // 따옴표로 감싼 구절 뒤에 누락/언급없음 류 표현이 오는 패턴
    const re =
        /['‘"“「]([^'’"”」]{2,30})['’"”」]\s*(?:[이가은는을를에의]{0,2}\s*(?:대한|관한)?\s*)?(?:누락|언급이 없|언급되지 않|빠져|미기재|기재되지 않|서술이 없|없어|없음|부족)/g
    let m: RegExpExecArray | null
    while ((m = re.exec(feedback)) !== null) {
        const phrase = m[1]
        if (phrase && normAnswer.includes(normalize(phrase))) {
            found.push(phrase)
        }
    }
    return found
}

// 피드백 구체성(실행 가능한 수정 지시) 판정: 단순 동사 등장이 아니라, 그 동사가 "~하세요/~바랍니다"류
// 지시문 어미와 짧은 거리 내에 결합된 경우만 인정한다. "수정신고"처럼 동사 음절을 포함하지만 지시와
// 무관한 세법 고유명사만으로는 통과하지 않도록, 판정 전에 알려진 오탐 용어를 먼저 제거한다.
// 주의(2026-07-21, 3.1-flash-lite 마이그레이션 회귀 리뷰): 한때 "재검토|학습|이해|파악|분석"을
// 동사 목록에 추가하고 간격도 넓혔었으나, "판례 법리에 대한 이해가 부족하여 재검토가 필요합니다"처럼
// 아무것도 구체적으로 지목하지 않는 순수 추상 문장까지 통과시키는 것이 실측으로 확인되어 되돌렸다 —
// 이 다섯 동사는 결핍을 "서술"할 뿐 수정을 "지시"하지 않아 Slice 1(피드백 구체화)의 취지와 반대다.
// 간격(40자)은 유지한다: 3.1-flash-lite가 실제로 만든 피드백 중 정당하게 구체적인 문장도 동사와
// 어미 사이가 멀어(예: "판례의 핵심 기준을 보완하고 절차적 권리 침해에 관한 법리를 다시 학습하시기
// 바랍니다") 10자 안팎으로는 못 잡는다 — 다만 이 넓은 간격 자체도 완벽하지 않아, 정당히 구체적인
// 일부 문장을 놓칠 수 있음(허위 양성보다 허위 음성이 안전한 실패 방향이라 이 트레이드오프를 수용).
const FEEDBACK_FALSE_POSITIVE_TERMS = ['수정신고']
function hasActionableRevisionInstruction(feedback: string): boolean {
    let stripped = feedback
    for (const term of FEEDBACK_FALSE_POSITIVE_TERMS) stripped = stripped.split(term).join('')
    return /(?:추가|수정|명시|구체화|보완|작성|제시|서술|설명)[^.!?]{0,40}(?:하세요|하십시오|하길|바랍니다|필요합니다|요망|해야|하여야|좋겠습니다|좋습니다)/.test(stripped)
}

